<h3 id="cc-m-hnh-ai-v-gi-ng-k-ultra">Các mô hình AI và Gói đăng ký Ultra</h3>
<p>
  Google đã giới thiệu các phiên bản Gemini mới với hiệu suất được công bố là
  vượt trội. Gemini 1.5 Pro, được gọi là Gemini Deepthank, được cho là đã đánh
  bại các mô hình suy luận khác như GPT-4o (phiên bản 03 được đề cập), thậm chí
  mô hình giá rẻ hơn là Gemini Flash cũng vượt qua GPT-4o về thứ hạng. Một điểm
  gây tranh cãi là việc ra mắt gói đăng ký Google AI Ultra mới để truy cập toàn
  bộ sức mạnh của Gemini, với mức giá được cho là rất cao: 124.99 USD/tháng
  trong 3 tháng đầu, sau đó tăng lên 249 USD/tháng, kèm theo ưu đãi tháng đầu
  miễn phí và dung lượng lưu trữ lớn 30 TB. Bên cạnh đó, Google cũng phát hành
  Gemma 3N, một mô hình mở (open model) có hiệu suất tiếp cận Claude Sonnet 3.7,
  tạo cơ hội cho các nhà phát triển sử dụng trong sản phẩm của họ.
</p>
<h3 id="s-tri-dy-ca-ai-agent-v-cng-c-t-ng-ha">
  Sự trỗi dậy của AI Agent và Công cụ tự động hóa
</h3>
<p>
  Xu hướng sử dụng AI làm agent (tác nhân) đang ngày càng rõ rệt. Google công bố
  chế độ Agent mới cho Gemini tích hợp trong trình duyệt Chrome (trước đây là
  Project Mariner). Chế độ này cho phép Gemini thực hiện các hành động trực tiếp
  trên website như click nút, điền form. Sự kiện cũng giới thiệu các công cụ dựa
  trên agent như Stitch, tự động tạo và thiết kế giao diện người dùng (UI), và
  Jules, một agent lập trình bất đồng bộ có khả năng viết mã cho các agent AI
  khác. Những công cụ này được cho là sẽ tự động hóa quy trình thiết kế và phát
  triển web, tích hợp với các công cụ như Figma. Google được ghi nhận vì đã hỗ
  trợ Model Context Protocol, giúp việc tích hợp các công nghệ này trở nên dễ
  dàng hơn cho nhà phát triển.
</p>
<h3 id="cc-d-n-ai-tin-tin-v-sng-to-ni-dung">
  Các Dự án AI Tiên tiến và Sáng tạo nội dung
</h3>
<p>
  Nhiều dự án AI đầy tham vọng đã được trình bày. Project Astra sử dụng nguồn
  cấp video trực tiếp và AI độ trễ thấp để mô tả thế giới xung quanh, được kỳ
  vọng sẽ tích hợp vào kính Android XR trong tương lai. Project Beam là một công
  cụ chuyển đổi video 2D thành trải nghiệm 3D. Các công cụ tạo nội dung media
  của Google, Videon (video) và Imagen (ảnh), nhận được nâng cấp đáng kể, đặc
  biệt Videon giờ đây có thể tạo video kèm theo âm thanh. Tất cả những khả năng
  này được kết hợp trong một công cụ mới gọi là Flow, nhắm mục tiêu đến các
  studio Hollywood và có thể được sử dụng để tạo ra nội dung cho các nền tảng
  như Netflix.
</p>
<h3 id="cp-nht-quan-trng-khc-css">Cập nhật quan trọng khác: CSS</h3>
<p>
  Ngoài trọng tâm về AI, một thông báo được nhấn mạnh là các primitive mới cho
